package cn.labzen.web.api.response.result;

/**
 * 标准结果集接口
 * <p>
 * 定义了 API 响应结果的基本结构，包含状态码、消息和数据值。
 * 该接口是项目中所有响应结果类型的通用契约，用于统一返回格式。
 * </p>
 */
public interface Result {

  /**
   * 获取响应状态码
   * <p>
   * 返回 HTTP 状态码或业务状态码，用于标识请求处理的结果状态。
   * 常见值：200（成功）、500（失败）等。
   * </p>
   *
   * @return HTTP 状态码或业务状态码
   */
  int code();

  /**
   * 获取响应消息
   * <p>
   * 返回请求处理状态的描述信息。对于成功的请求，可以是空字符串；
   * 对于失败的请求，通常包含错误描述信息。
   * 传递一个前后端统一的 message code 来做国际化，是好的实践。
   * </p>
   *
   * @return 响应消息文本
   */
  String message();

  /**
   * 获取响应数据值
   * <p>
   * 返回实际的响应数据内容，可以是任意对象类型。
   * 对于无数据的响应（如纯状态通知），可以是 {@code null}。
   * </p>
   *
   * @return 响应数据对象
   */
  Object value();
}
