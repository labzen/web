package cn.labzen.web.file.definition.enums;

public enum Alignment {

  /**
   * 默认水平对齐
   */
  HORIZONTAL,
  /**
   * 居中对齐，可视为水平和垂直的居中
   */
  CENTER,
  /**
   * 水平左对齐
   */
  LEFT,
  /**
   * 水平右对齐
   */
  RIGHT,
  /**
   * 垂直上对齐
   */
  TOP,
  /**
   * 垂直下对齐
   */
  BOTTOM,
  /**
   * 水平填充对齐
   */
  FILL,
  /**
   * 水平和垂直两端对齐
   */
  JUSTIFY,
  /**
   * 水平和垂直分散对齐
   */
  DISTRIBUTED
}
